_LEAK_PATTERNS = [
    "argmin reg in sliding_window", "argmax reg in sliding_window",
    "count r in all_residues", "exists reg in sliding_window",
    "filter (i,", "size(filter",
    "mean_plddt(", "mean_pae(", "mean_rel_sasa(",
    "rel_sasa(residue", "n_neighbors(residue", "ss(residue",
    "distance(residue", "radius_of_gyration(", "contact_density(",
    "plddt(r)", " ss(r)",
]


def assert_no_leaks() -> None:
    """Crash early if any paraphrase contains DSL syntax that would
    leak the canonical program."""
    for tname, pool in PARAPHRASE_POOLS_V3.items():
        for i, p in enumerate(pool):
            for pat in _LEAK_PATTERNS:
                if pat in p:
                    raise AssertionError(
                        f"DSL-leak in {tname}[{i}]: {p!r} (pattern: {pat!r})"
                    )


def stats():
    print(f"{'Template':<10} {'Paraphrases':>11} {'Unique':>6}")
    print("-" * 32)
    total = 0
    for name, pool in sorted(PARAPHRASE_POOLS_V3.items()):
        print(f"  {name:<8} {len(pool):>11} {len(set(pool)):>6}")
        total += len(pool)
    print(f"\nTotal: {total} paraphrase forms across {len(PARAPHRASE_POOLS_V3)} templates")


if __name__ == "__main__":
    assert_no_leaks()
    print("✓ no DSL-leak detected")
    print()
    stats()
